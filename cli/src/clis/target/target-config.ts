import fs from 'fs';
import path from 'path';
import yaml from 'js-yaml';

export interface TargetConfig {
  mode: 'local' | 'remote';
  host?: string;
  user?: string;
  ssh_key?: string;
}

interface IacToolboxYamlWithTarget {
  [key: string]: unknown;
  target?: {
    mode?: string;
    host?: string;
    user?: string;
    ssh_key?: string;
  };
}

/**
 * Resolve the path to iac-toolbox.yml.
 *
 * When an explicit `filePath` is provided (e.g. via --filePath flag) it takes
 * priority. Otherwise falls back to `<destination>/iac-toolbox.yml`.
 */
function resolveConfigPath(destination: string, filePath?: string): string {
  if (filePath) {
    return path.resolve(filePath);
  }
  return path.join(destination, 'iac-toolbox.yml');
}

/**
 * Read the target section from iac-toolbox.yml.
 * Returns local defaults if the file or target section is absent.
 */
export function loadTargetConfig(destination: string): TargetConfig {
  const configPath = resolveConfigPath(destination);

  if (!fs.existsSync(configPath)) {
    return { mode: 'local' };
  }

  try {
    const content = fs.readFileSync(configPath, 'utf-8');
    const parsed = (yaml.load(content) as IacToolboxYamlWithTarget) || {};

    if (!parsed.target) {
      return { mode: 'local' };
    }

    const t = parsed.target;
    const mode = t.mode === 'remote' ? 'remote' : 'local';

    if (mode === 'remote') {
      return {
        mode: 'remote',
        host: t.host,
        user: t.user,
        ssh_key: t.ssh_key,
      };
    }

    return { mode: 'local' };
  } catch {
    return { mode: 'local' };
  }
}

/**
 * Write or overwrite the target section in iac-toolbox.yml.
 * Preserves all other keys in the file.
 */
export function updateTargetConfig(
  destination: string,
  config: TargetConfig
): void {
  const configPath = resolveConfigPath(destination);
  let existing: IacToolboxYamlWithTarget = {};

  if (fs.existsSync(configPath)) {
    try {
      const content = fs.readFileSync(configPath, 'utf-8');
      existing = (yaml.load(content) as IacToolboxYamlWithTarget) || {};
    } catch {
      existing = {};
    }
  }

  if (config.mode === 'local') {
    existing.target = { mode: 'local' };
  } else {
    existing.target = {
      mode: 'remote',
      host: config.host,
      user: config.user,
      ssh_key: config.ssh_key,
    };
  }

  // Ensure directory exists
  const dir = path.dirname(configPath);
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
  }

  const yamlContent =
    '# Generated by iac-toolbox\n# Safe to commit — no secrets stored here\n' +
    yaml.dump(existing, { lineWidth: -1, quotingType: '"' });

  fs.writeFileSync(configPath, yamlContent, 'utf-8');
}

/**
 * Build the RPI_* env vars consumed by Ansible inventory.
 * Falls back to safe local defaults when no target config exists.
 */
export function buildTargetEnv(destination: string): {
  RPI_SSH_KEY: string;
  RPI_LOCAL: string;
} {
  const config = loadTargetConfig(destination);

  if (config.mode === 'remote') {
    return {
      RPI_SSH_KEY: config.ssh_key ?? '~/.ssh/id_ed25519',
      RPI_LOCAL: 'false',
    };
  }

  // local mode or no config — return backward-compatible defaults
  return {
    RPI_SSH_KEY: '~/.ssh/id_ed25519',
    RPI_LOCAL: 'true',
  };
}
